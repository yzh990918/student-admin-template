<template>
  <div class="login-container">
    <el-form ref="loginForm" :model="loginForm" :rules="loginRules" class="login-form" auto-complete="on" label-position="left">

      <div class="title-container">
        <h3 class="title">学生信息管理系统</h3>
      </div>

      <el-form-item prop="username">
        <span class="svg-container">
          <svg-icon icon-class="user" />
        </span>
        <el-input
          ref="username"
          v-model="loginForm.username"
          placeholder="用户名"
          name="username"
          type="text"
          tabindex="1"
          auto-complete="on"
        />
      </el-form-item>
      <el-form-item prop="password">
        <span class="svg-container">
          <svg-icon icon-class="password" />
        </span>
        <el-input
          :key="passwordType"
          ref="password"
          v-model="loginForm.password"
          :type="passwordType"
          placeholder="密码"
          name="password"
          tabindex="2"
          auto-complete="on"
          @keyup.enter.native="handleLogin"
        />
        <span class="show-pwd" @click="showPwd">
          <svg-icon :icon-class="passwordType === 'password' ? 'eye' : 'eye-open'" />
        </span>
      </el-form-item>
      <div class="options">
        <el-link type="primary" @click="toTeacherRegister">教师注册</el-link>
        <el-link type="primary" @click="toStudentRegister">学生注册</el-link>
      </div>
      <el-button type="primary" style="width:100%;margin-bottom:30px;" @click.native.prevent="handleLogin">登录</el-button>
    </el-form>
  </div>
</template>

<script>
export default {
  name: 'Login',
  data() {
    const validatePassword = (rule, value, callback) => {
      if (value.length < 5) {
        callback(new Error('密码不能小于五位数'))
      } else {
        callback()
      }
    }
    return {
      loginForm: {
        username: 'admin',
        password: '12345'
      },
      // 校验规则
      loginRules: {
        username: [{ required: true, trigger: 'blur', message: '用户名是必需参数' }],
        password: [{ required: true, trigger: 'blur', validator: validatePassword }]
      },
      loading: false,
      passwordType: 'password',
      redirect: undefined
    }
  },
  watch: {
    $route: {
      // 判断当前页面的重定向
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
      // 点击密码显示按钮 让密码框聚焦
      this.$nextTick(() => {
        this.$refs.password.focus()
      })
    },
    toTeacherRegister() {
      this.$router.push(`/register?type=2`)
    },
    toStudentRegister() {
      this.$router.push('/register?type=1')
    },
    handleLogin() {
      this.$refs.loginForm.validate(valid => {
        if (valid) {
          // this.loading = true
          // 提交action 并请求服务端数据 返回promise对象
          this.$store.dispatch('user/login', this.loginForm).then(() => {
            // 路由重定向 跳转首页
            this.$router.push({ path: this.redirect || '/' })
            // this.loading = false
            this.$message.success('登录成功')
          }).catch(() => {
            // this.loading = false
          })
        } else {
          // this.loading = false
          return false
        }
        console.log(this.loading)
      })
    }
  }
}
</script>

